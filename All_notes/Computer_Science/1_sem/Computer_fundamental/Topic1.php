                        <h5>1. Input Device </h5>
                        <p class="excert">
                        Input Device वे Device होते है जिनके द्वारा हम अपने डाटा या निर्देशों को Computer में Input करा सकते हैं| Computer में कई Input Device होते है 
                        ये Devices Computer के मस्तिष्क को निर्देशित करती है की वह क्या करे? Input Device कई रूप में उपलब्ध है 
                        तथा सभी के विशिष्ट उद्देश्य है टाइपिंग के लिये हमारे पास Keyboard होते है, 
