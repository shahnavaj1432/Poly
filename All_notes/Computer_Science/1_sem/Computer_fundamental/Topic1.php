                     <h5>2. C.P.U.</h5>
                     <p>
                     C.P.U का पूरा नाम सेन्ट्रल प्रोसेसिंग यूनिट (Central Processing Unit) हैं|
                      इसका हिंदी नाम केन्द्रीय संसाधन इकाई होता हैं| यह Computer का सबसे महत्वपूर्ण भाग होता हैं|
                       अर्थात इसके बिना Computer सिस्टम पूर्ण नहीं हो सकता है, इससे सभी Device जुड़े हुए रहते है 
                       जैसे- Keyboard, Mouse, Monitor आदि | इसे Computer का मष्तिस्क (Mind) भी कहते है| 
                       इसका मुख्य कार्य प्रोग्राम (Programs) को क्रियान्वित (Execute) करना है
                      इसके आलावा C.P.U Computer के सभी भागो, जैसे- Memory, Input, Output Devices 
                      के कार्यों को भी नियंत्रित करता हैं| 
                     </p>
